/// Size tokens for controls, icons, and layout constraints.
abstract final class DSSize {
  /// Minimum interactive area recommended for touch targets.
  static const double minTouchTarget = 48;

  /// Small control height.
  static const double controlSm = 32;

  /// Medium control height.
  static const double controlMd = 40;

  /// Large control height.
  static const double controlLg = 48;

  /// Extra-small icon size.
  static const double iconXs = 12;

  /// Small icon size.
  static const double iconSm = 16;

  /// Medium icon size.
  static const double iconMd = 20;

  /// Large icon size.
  static const double iconLg = 24;

  /// Extra-large icon size.
  static const double iconXl = 32;

  /// Maximum content width for constrained page layouts.
  static const double maxContentWidth = 1200;
}
