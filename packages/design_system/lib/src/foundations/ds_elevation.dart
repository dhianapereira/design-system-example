/// Elevation tokens used to describe component depth.
abstract final class DSElevation {
  /// No elevation.
  static const double none = 0;

  /// Subtle elevation for raised controls.
  static const double sm = 1;

  /// Medium elevation for floating surfaces.
  static const double md = 3;

  /// Large elevation for overlays.
  static const double lg = 6;

  /// Extra-large elevation for modals and drawers.
  static const double xl = 12;
}
