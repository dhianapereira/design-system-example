/// Opacity tokens used to represent emphasis and interaction states.
abstract final class DSOpacity {
  /// Fully transparent.
  static const double transparent = 0;

  /// Opacity for disabled content.
  static const double disabled = 0.38;

  /// Opacity for subtle overlays.
  static const double subtle = 0.08;

  /// Opacity for hover overlays.
  static const double hover = 0.12;

  /// Opacity for pressed overlays.
  static const double pressed = 0.16;

  /// Fully opaque.
  static const double opaque = 1;
}
