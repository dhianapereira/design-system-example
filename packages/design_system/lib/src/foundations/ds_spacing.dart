/// Spacing tokens used to keep component layout consistent.
abstract final class DSSpacing {
  /// Removes spacing between elements.
  static const double none = 0;

  /// Extra-extra-small spacing. Useful for dense visual adjustments.
  static const double xxs = 2;

  /// Extra-small spacing.
  static const double xs = 4;

  /// Small spacing.
  static const double sm = 8;

  /// Medium spacing.
  static const double md = 12;

  /// Large spacing.
  static const double lg = 16;

  /// Extra-large spacing.
  static const double xl = 24;

  /// Extra-extra-large spacing.
  static const double xxl = 32;

  /// Extra-extra-extra-large spacing.
  static const double xxxl = 40;

  /// Largest default spacing used for page-level separation.
  static const double huge = 64;
}
