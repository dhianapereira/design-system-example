/// Border width tokens used by inputs, dividers, and focus indicators.
abstract final class DSBorderWidth {
  /// Removes the border from a component.
  static const double none = 0;

  /// Thin border for subtle separation.
  static const double thin = 1;

  /// Medium border for selected or emphasized states.
  static const double medium = 2;

  /// Thick border for strong focus indicators.
  static const double thick = 4;
}
